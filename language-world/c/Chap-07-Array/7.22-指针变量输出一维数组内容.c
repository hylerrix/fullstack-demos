#include <stdio.h>

/*在二维数组中，用指向数组元素和
指向分数组的指针变量，按行输出二维数组各元素的值*/

int main(){
    int b[3][5]={{31,32,33,24,25},{16,17,8,9,10},{19,11,51,14,15}};
    int *q;/* q是指向整形的指针变量 */
    int (*p)[5];/* p是指向一维数组的指针变量 */
    /* *p[5]中由于[]优先级高，将定义为5个元素的数组指针 */
    for(p=b;p<b+3;p++){/* 用p指向各行数组 */
        for(q=*p;q<*p+5;q++)/* 用*p指向各行数组的首元素 */
            printf("%5d",*q);
        printf("\n");
    }
    return 0;
}
